export function buildCrisisConversionPrompt(extraInfo?: string): string {
  const trimmedExtra = (extraInfo || '').trim()
  const extraBlock = trimmedExtra
    ? `\n\n【教练本次补充信息】\n${trimmedExtra}`
    : '\n\n【教练本次补充信息】\n无。请仅基于系统已加载的客户档案、正式健康数据和本次附件进行判断。'

  return `请基于当前客户的个体健康档案、已加载的正式数据、教练本次补充信息以及本次上传附件，生成一份“销转下危机”专用教练话术。

【任务目标】
帮助教练把客户当前身体风险讲清楚，让客户意识到健康管理的必要性，并自然承接到我们的健康管理产品。请输出给教练使用，不要直接假装正在和客户本人对话。

【重要边界】
1. 不要把教练补充信息、附件识别内容或推测内容写成 official / formal 事实；请区分“已知事实”“附件/补充提示”“需要复查确认”。
2. 不要做疾病确诊、用药建议或治疗承诺；涉及诊断、用药、肿瘤、心脑血管急症等必须提示线下医生/专科复查。
3. 可以有风险感和紧迫感，但不要绝对化恐吓，不要编造未出现的指标、病史或检查结论。
4. 产品承接要围绕“持续监测、饮食运动执行、复查提醒、风险跟踪、教练陪跑”，不要承诺治愈或替代医疗。

【输出结构】
1. 核心问题总览：用 3-5 条列出当前最值得教练抓住的风险主线。
2. 递进式下危机逻辑：按“第一层紧急风险 / 第二层隐藏加重因素 / 第三层长期隐患 / 第四层多器官连锁影响”组织，每层包含：
   - 已知依据
   - 风险解释
   - 教练可直接说的话术
3. 销转承接话术：给出 2-3 段自然过渡到健康管理产品的话术，强调为什么现在需要系统管理。
4. 问诊追问清单：列出教练在成交前应补问的 6-10 个关键问题。
5. 医疗边界提醒：列出必须建议客户就医/复查的情况。

【风格要求】
语言要像资深健康管理教练，清晰、有压迫感但不失专业和分寸；避免空泛鸡汤，尽量把“风险为什么会发生、如果不管会怎样、现在管理能阻断什么”讲明白。${extraBlock}`
}

